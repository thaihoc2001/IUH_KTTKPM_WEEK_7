package com.example.templateService.dto;

import java.util.Date;

public class ChuyenBayDto {
    private String gaDi;
    private String gaDen;
    private Date gioDi;
    private Date gioDen;
    private int doDai;
    private int chiPhi;
    
    public String getGaDi() {
        return gaDi;
    }
    public void setGaDi(String gaDi) {
        this.gaDi = gaDi;
    }
    public String getGaDen() {
        return gaDen;
    }
    public void setGaDen(String gaDen) {
        this.gaDen = gaDen;
    }
    public Date getGioDi() {
        return gioDi;
    }
    public void setGioDi(Date gioDi) {
        this.gioDi = gioDi;
    }
    public Date getGioDen() {
        return gioDen;
    }
    public void setGioDen(Date gioDen) {
        this.gioDen = gioDen;
    }
    public int getDoDai() {
        return doDai;
    }
    public void setDoDai(int doDai) {
        this.doDai = doDai;
    }
    public int getChiPhi() {
        return chiPhi;
    }
    public void setChiPhi(int chiPhi) {
        this.chiPhi = chiPhi;
    }


}
